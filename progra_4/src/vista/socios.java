    private void eliminarSocioEliminarBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarSocioEliminarBotonMouseClicked
        // TODO add your handling code here:
  
        if(!(socioEliminarCombobox.getSelectedItem().equals(""))){
            for(int i = 0; i < Progra4.cenDep.getListaSocios().size(); i++){
                if(socioEliminarCombobox.getSelectedItem().equals(Progra4.cenDep.getListaSocios().get(i).getNombre())){
                    metodosBase met = new metodosBase();
                    met.eliminarSocioBase(Progra4.cenDep.getListaSocios().get(i).getIdSocio());
                    Progra4.cenDep.getListaSocios().remove(i);
                    cargarComboboxSocios();
                }
            }
        }
    }//GEN-LAST:event_eliminarSocioEliminarBotonMouseClicked


    public void cargarComboboxSocios(){
        
        socioEliminarCombobox.removeAllItems();
        int tam = Progra4.cenDep.getListaSocios().size();
                
        for(int i = 0; i < tam; i++){
            socioEliminarCombobox.addItem(Progra4.cenDep.getListaSocios().get(i).getNombre());
        }
    }
    
    
