    private void agregarSocioAgregarBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agregarSocioAgregarBotonMouseClicked
        // TODO add your handling code here:
        
        String nombre = nombreSocioAgregarTextField.getText();
        String cedula = cedulaSocioAgregarTextField.getText();
        String telefono = telefonoSocioAgregarTextField.getText();
        String provincia = provinciaSocioAgregarTextField.getText();
        String ciudad = ciudadSocioAgregarTextField.getText();
        String direccion = direccionSocioAgregarTextField.getText();
        int cuota = Integer.parseInt(cuotaSocioAgregarTextField.getText());
        
        Socios per = new Socios(nombre, cedula, telefono, provincia, ciudad, direccion, cuota);
        Progra4.cenDep.getListaSocios().add(per);
        
        metodosBase met = new metodosBase();
        met.insertarSocioBase(nombre, cedula, telefono, provincia, ciudad, direccion, cuota);
        
        cargarComboboxSocios();
        
        nombreSocioAgregarTextField.setText("");
        cedulaSocioAgregarTextField.setText("");
        telefonoSocioAgregarTextField.setText("");
        provinciaSocioAgregarTextField.setText("");
        ciudadSocioAgregarTextField.setText("");
        direccionSocioAgregarTextField.setText("");
        cuotaSocioAgregarTextField.setText("");
    }//GEN-LAST:event_agregarSocioAgregarBotonMouseClicked

